
package forkertest;

public class Player {
     private int ID;
     private String Name;
     private int Points;

    public void setID(int ID) {
        this.ID = ID;
    }

    public void setName(String Name) {
        this.Name = Name;
    }

    public void setPoints(int Points) {
        this.Points = Points;
    }

    public int getID() {
        return ID;
    }

    public String getName() {
        return Name;
    }

    public int getPoints() {
        return Points;
    }
   
   
    
    
}
